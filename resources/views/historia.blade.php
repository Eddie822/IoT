        <ul>
            <li>El concepto que daría lugar al IoT comienza con el
                desarrollo de los primeros dispositivos de computación y
                redes. Durante los años 60 y 70, se avanzó en tecnologías de
                comunicación, pero los dispositivos aún no estaban
                conectados como lo están hoy.</li>
        </ul>
        <ul>
            <li>En 1982, la primera "máquina conectada" fue una máquina
