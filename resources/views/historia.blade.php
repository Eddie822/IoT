            (2000s)</p>
    </div>
    <div class="text-center mt-2 fs-1 mx-5">
        <ul>
            <li>Durante la primera década del siglo XXI, el IoT comenzó a
                ganar tracción debido a los avances en tecnologías como los
                sensores, la conectividad inalámbrica, el Wi-Fi, los protocolos de red y los smartphones.</li>
        </ul>
        <ul>
            <li>En 2008, el número de dispositivos conectados a internet
                superó el número de personas en el mundo. Esto marcó un
                hito importante, y organizaciones como Cisco señalaron que
                el IoT estaba en pleno crecimiento.</li>
        </ul>
        <ul>
            <li>Los avances en el IPv6 también jugaron un papel clave al
                permitir la conexión de miles de millones de dispositivos
                mediante direcciones IP únicas.</li>
        </ul>
    </div>

    <div class=" mb-5 text-center display-3 mt-5 font-weight-bold text-primary">
        <p> Crecimiento y adopción masiva
            (2010s)</p>
    </div>
    <div class="text-center mt-2 fs-1 mx-5">
        <ul>
            <li> En los años 2010, el IoT dejó de ser un concepto
                experimental y empezó a integrarse en productos
                comerciales. La llegada de dispositivos domésticos
                inteligentes como el Nest Thermostat en 2011 y los
                asistentes virtuales como Amazon Alexa y Google Home
                popularizaron el IoT en los hogares.</li>
        </ul>
