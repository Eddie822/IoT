    <div class=" mb-5 text-center display-3 mt-5 font-weight-bold text-primary">
        <p>Nacimiento del concepto (1990s)</p>
    </div>
    <div class="text-center mt-2 fs-1 mx-5">
        <ul>
            <li>En 1991, Mark Weiser, un científico del Xerox PARC, introdujo
                la idea de la "computación ubicua", en la que los dispositivos
                tecnológicos serían omnipresentes, pero casi invisibles en
                nuestra vida diaria.</li>
        </ul>
        <ul>
            <li> En 1999, Kevin Ashton, un investigador británico, acuñó el
                término "Internet de las Cosas" mientras trabajaba en el
                Massachusetts Institute of Technology (MIT). Ashton
                propuso conectar objetos físicos a internet usando etiquetas
                RFID (Radio Frequency Identification) para mejorar el
                seguimiento en la cadena de suministro. Esta idea sentó las
                bases de lo que hoy se entiende por IoT.</li>
        </ul>
    </div>

